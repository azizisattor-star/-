export function getRandomTask(type: 'truth' | 'dare', level: 'hot' | 'dirty' | 'extreme'): string {
  const list = type === 'truth' ? TRUTH_QUESTIONS[level] : DARE_ACTIONS[level];
  const idx = Math.floor(Math.random() * list.length);
  return list[idx];
}
